// 이코테 DFS/BFS 실전문제

const solution = (N, M, ice_tray) => {
    let answer = 0;

    const dfs = (x, y) => {
        if (x <= -1 || x >= N || y <= -1 || y >= M) return false;

        if (ice_tray[x][y] === 0) {
            ice_tray[x][y] = 1;
            dfs(x - 1, y);
            dfs(x + 1, y);
            dfs(x, y - 1);
            dfs(x, y + 1);
            return true;
        }
        return false;
    };

    for (let i = 0; i < N; i++) {
        for (let j = 0; j < M; j++) {
            if (dfs(i, j) === true) {
                answer++;
            }
        }
    }

    return answer;
};

